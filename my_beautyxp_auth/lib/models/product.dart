class Product {
  final String name;
  final String category;
  final String skinType;
  final String budget;
  final int price;
  final String description;
  final String image;

  Product({
    required this.name,
    required this.category,
    required this.skinType,
    required this.budget,
    required this.price,
    required this.description,
    required this.image,
  });
}